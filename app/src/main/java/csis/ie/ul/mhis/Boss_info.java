public class Boss_info {
    private String _id;
    private String _name;
    private String _type;
    private String _weakness;
    private String _element;

    public String get_id() {
        return _id;
    }

    public void set_id(String _id) {
        this._id = _id;
    }

    public String get_name() {
        return _name;
    }

    public void set_name(String _name) {
        this._name = _name;
    }

    public String get_type() {
        return _type;
    }

    public void set_type(String _type) {
        this._type = _type;
    }

    public String get_weakness() {
        return _weakness;
    }

    public void set_weakness(String _weakness) {
        this._weakness = _weakness;
    }

    public String get_element() {
        return _element;
    }

    public void set_element(String _element) {
        this._element = _element;
    }
}
